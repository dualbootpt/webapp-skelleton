                        <div class="tab-pane" id="notificacao_en">
                                <br>
                                <strong>
                                Adverse Reaction is a response to a medicinal product which is noxious and unintended:
                                </strong>
                                This includes adverse reactions which arise from:
                                <p class="smallfont">   
                                    <ul>
                                        <li>Use of a medicinal product within the terms of the marketing authorisation;</li>
                                        <li>Use outside the terms of the marketing authorisation, including overdose, misuse, abuse and medication errors;</li>
                                        <li>Occupational exposure.</li>
                                    </ul>
                                </p>
                            <strong>TYPES OF ADVERSE REACTIONS</strong>
                                <p>
                                <p class="smallfont">   
                                    <b>Serious Adverse Reaction</b>
                                    <br>
                                    Any adverse reaction that is life-threatening, leads to death, requires hospitalization or its prolongation, results in a significant or persistent disability, involves a congenital anomaly, or also if it is classified as medically important.
                                    <br><b>Unexpected Adverse Drug Reaction</b>
                                    <br>
                                    An adverse reaction whose nature, severity, intensity, or effect is incompatible or not described in the Summary of Product Characteristics.
                                    <br><b>Quality problems</b>
                                    <br>
                                    Non-compliance of drug parameters (altered integrity, visible abnormalities and contamination by microorganisms and other changes in the final product).
                                    <br><b>Lack of Therapeutic Effect</b>
                                    <br>
                                    Absent effectiveness of product / drug.
                                    <br><b>Other Problems</b>
                                    <br>
                                    Labelling errors in the Product Information Leaflet (PIL) and / or packaging of the product / drug, which may have some interference, namely in the therapeutic compliance and effectiveness or in the exacerbation of the adverse reactions.
                                    <br><b></b>
                                </p>
                            <strong>SEVERITY</strong>
                                <p>
                                <p class="smallfont">   
                                    The severity is related with the evolution/outcome of the adverse reaction, being defined by the criteria described for a severe adverse reaction.
                                </p>
                            <strong>INTENSITY</strong>
                                <p>
                                <p class="smallfont">   
                                    The intensity or severity is based on the intensity of the adverse reaction, being classified into mild, moderate and severe.
                                </p>
                            <strong>ASSESSMENT OF CAUSALITY</strong>
                                <p>
                                <p class="smallfont">   
                                    The causal attribution refers to a clinical judgment that evaluates if a product/drug could be responsible for the emergence of an adverse reaction.
                                    <br>
                                    Once proven or not a causal relation between the adverse reaction and the drug/product, a decision is taken, which eventually lead to contact the competent authorities and other health professionals, and apply the appropriate safety measures.
                                </p>
                            <strong>SPONTANEOUS NOTIFICATION</strong>
                                <p>
                                <p class="smallfont">   
                                    The spontaneous reporting of adverse reactions by health professionals or consumers is valuable information that needs to be collected, interpreted and then 'returned' to the medical community and patients. This ensures access to accurate and most up to date information on the safety of medicines.
                                    <br>
                                    All this activities allows detection of rare adverse effects or specific ones to certain races, geographic regions or different clinical settings.
                                    <br>
                                    The evaluation of safety signals is fundamental in the notification process.
                                    <br>
                                    <br><b>What to notify?</b>
                                    <br>
                                    All suspected serious adverse reactions, even those already described in the Summary of Product Characteristics.
                                    <br>
                                    All suspected adverse reactions not described in the Summary of Product Characteristics, serious or non-serious.
                                    <br>
                                    All suspicions of an increased frequency in adverse reaction, whether severe or non-severe.
                                </p>                            
