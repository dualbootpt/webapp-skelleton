                            <br>
                            <strong>WHAT IS IT - PHARMACOVIGILANCE SYSTEM</strong>
                            <p>
                            <p class="smallfont">
                                Pharmacovigilance is "the process and the science of detection and assessment of adverse reactions, in order to reduce the risks and increase the benefits of the medicinal products." (European Commission, 2007).
Pharmacovigilance aims at detecting, recording and assessing adverse reactions to drugs and medical devices in order to prevent them.
With the purpose of ensuring the protection of public health and make the use of medicinal products safer and more rational, Pharmacovigilance is a responsibility shared between the several players involved:
                                <ul>
                                    <li> INFARMED/National and International Regulatory Authorities; </li>
                                    <li> Health Professionals (doctors, pharmacists, nurses); </li>
                                    <li> Pharmaceutical Industry; </li>
                                    <li> Patients/Consumers. </li>
                                </ul>    
                            </p>
                            <strong>THE IMPORTANCE – SAFETY MONITORING</strong>
                            <p>
                            <p class="smallfont">
                                Through continuous and effective monitoring of drug safety it is possible to inspire and build the confidence of patients, health professionals and society as a whole.
                            <br>
                                Therefore, it is fundamental that during the entire lifecycle of the medicinal product its safety is monitored, not only for the effective and early detection of adverse reactions, as well as to complete and improve the safety information of that medicinal product to be always possible to detect new evidences regarding their safety profile.
                            </p>
                            <strong>SAFETY SIGNS</strong>
                            <p>
                            <p class="smallfont">
                                In Pharmacovigilance, a safety sign consists in a hypothesis supported by data which may or may not justify the relation between the event and the suspected drug. Thus, it is a warning that needs further investigation. They can come from various sources used in Pharmacovigilance throughout the cycle of the drug, both pre and post-marketing like.
                            </p>
